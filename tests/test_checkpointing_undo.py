class TestRestoreTournament:
    def test_returns_none_for_missing(self):
        result = cph.restore_tournament("nonexistent", lambda uid: None)
        assert result is None

    def test_restores_tournament_id(self, p1):
        t = Tournament(title="X", scope="ch", seed=42)
        cph.save_checkpoint(t)
        restored = cph.restore_tournament(
            t.tournament_id, lambda uid: None)
        assert restored.tournament_id == t.tournament_id

    def test_restores_players(self, p1, p2):
        t = Tournament(title="X", scope="ch", seed=42)
        t.players = [p1, p2]
        cph.save_checkpoint(t)
        users_db = {p1.uid.id: p1, p2.uid.id: p2}
        restored = cph.restore_tournament(
            t.tournament_id, lambda uid: users_db.get(uid))
        assert len(restored.players) == 2
        assert p1 in restored.players
        assert p2 in restored.players

    def test_restores_drop_dict(self, p1, p2):
        t = Tournament(title="X", scope="ch", seed=42)
        t.players = [p1, p2]
        t.drop_dict[p1].append(0)
        cph.save_checkpoint(t)
        users_db = {p1.uid.id: p1, p2.uid.id: p2}
        restored = cph.restore_tournament(
            t.tournament_id, lambda uid: users_db.get(uid))
        assert p1 in restored.drop_dict
        assert restored.drop_dict[p1] == [0]

    def test_restores_dq_dict(self, p1, p2):
        t = Tournament(title="X", scope="ch", seed=42)
        t.players = [p1, p2]
        t.dq_dict[p2].append(1)
        cph.save_checkpoint(t)
        users_db = {p1.uid.id: p1, p2.uid.id: p2}
        restored = cph.restore_tournament(
            t.tournament_id, lambda uid: users_db.get(uid))
        assert p2 in restored.dq_dict
        assert restored.dq_dict[p2] == [1]

    def test_restores_checkins_post_id(self):
        t = Tournament(title="X", scope="ch", seed=42)
        t.checkins_post_id = 999
        cph.save_checkpoint(t)
        restored = cph.restore_tournament(t.tournament_id, lambda uid: None)
        assert restored.checkins_post_id == 999

    def test_missing_user_skipped(self, p1):
        t = Tournament(title="X", scope="ch", seed=42)
        t.players = [p1]
        cph.save_checkpoint(t)
        # Lookup returns None for all users.
        restored = cph.restore_tournament(
            t.tournament_id, lambda uid: None)
        assert restored.players == []


