class TestEdgeCases:
    def test_save_checkpoint_with_no_phase(self):
        # Tournament has no phases yet — should still save successfully.
        t = Tournament(title="X", scope="ch", seed=42)
        rowid = cph.save_checkpoint(t)
        assert rowid is not None

    def test_save_checkpoint_returns_none_on_error(self):
        # Tournament.save_checkpoint wraps in try/except, returns None on error.
        # We can't easily simulate a DB error; just verify no crash on edge inputs.
        t = Tournament(title="X", scope="ch", seed=42)
        result = t.save_checkpoint()
        # Either an int rowid or None — never raises.
        assert isinstance(result, int) or result is None

    def test_restore_after_multiple_saves_returns_latest(self):
        t = Tournament(title="X", scope="ch", seed=42)
        cph.save_checkpoint(t)
        t.checkins_post_id = 100
        cph.save_checkpoint(t)
        t.checkins_post_id = 200
        cph.save_checkpoint(t)
        restored = cph.restore_tournament(t.tournament_id, lambda uid: None)
        assert restored.checkins_post_id == 200
