class TestUndoMatch:
    def test_undo_resets_node(self, p1):
        from axi.tournament_presets import register_preset, PRESETS
        if "se_undo_test" not in PRESETS:
            @register_preset("se_undo_test")
            def _preset(_t):
                return [lambda tt, ps: SingleElimination(tt, ps, stream=False)], "SE"

        t = Tournament(title="T", scope="ch", seed=42)
        t.add_players(make_players(4, p1))
        t.preset("se_undo_test")
        t.begin()
        graph = t.current_phase()
        active = [n for n in graph.nodes_by_id.values()
                  if n.awake() and n.label != "VICTORY"]
        node = active[0]
        winner = node.players[0]
        _complete(graph, node, winner)
        assert node.completed()
        graph.undo_match(node)
        assert node.status == MATCH_STATUS_ASLEEP
        assert node.score == [0, 0]

    def test_undo_releases_placement(self, p1):
        from axi.tournament_presets import register_preset, PRESETS
        if "se_undo_test2" not in PRESETS:
            @register_preset("se_undo_test2")
            def _preset(_t):
                return [lambda tt, ps: SingleElimination(tt, ps, stream=False)], "SE"
        t = Tournament(title="T", scope="ch", seed=42)
        t.add_players(make_players(4, p1))
        t.preset("se_undo_test2")
        t.begin()
        graph = t.current_phase()
        active = [n for n in graph.nodes_by_id.values()
                  if n.awake() and n.label != "VICTORY" and n.loser_gets is not None]
        if not active:
            pytest.skip("no node with loser_gets in this preset config")
        node = active[0]
        loser = node.players[1]
        _complete(graph, node, node.players[0])
        # Loser got placed.
        assert graph.placements_dict.get(loser) == node.loser_gets
        graph.undo_match(node)
        # Placement released.
        assert graph.placements_dict.get(loser, -1) == -1


