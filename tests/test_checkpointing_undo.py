def make_players(n, p1):
    return [type(p1)(f"p{i}", 2000 + i) for i in range(n)]


def _complete(graph, node, winner):
    idx = node.players.index(winner)
    ft = node.first_to() if node.first_to() > 0 else 1
    score = [0, 0]
    score[idx] = ft
    node.score = score
    return graph.complete_match(node)


