class TestMatchNodeUndo:
    def test_clears_score_and_status(self):
        node = MatchNode(tournament_id="t", graph_id="g",
                         players=[], status=MATCH_STATUS_COMPLETED,
                         score=[2, 0])
        node.undo()
        assert node.status == MATCH_STATUS_ASLEEP
        assert node.score == [0, 0]

    def test_clears_checkins_and_reports(self):
        node = MatchNode(tournament_id="t", graph_id="g", players=[])
        node.checkins.add("user1")
        node.reports["user1"] = (2, 0)
        node.undo()
        assert node.checkins == set()
        assert node.reports == {}

    def test_clears_checkin_deadline(self):
        node = MatchNode(tournament_id="t", graph_id="g", players=[])
        node.checkin_deadline = 9999
        node.undo()
        assert node.checkin_deadline is None


